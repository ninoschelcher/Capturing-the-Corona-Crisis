      .style("class", "axis")
      .style("font-size", "30px")
      .attr("transform", "translate(11,60)")
      .call(yAxis);

    g.selectAll("text").attr("transform", "translate(10,0)");
  },
  created() {
    AOS.init({
      duration: 400,
      easing: "ease-in-out",
    });
  },
